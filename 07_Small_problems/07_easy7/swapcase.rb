UPPERCASE = ("A".."Z")
LOWERCASE = ("a".."z")

def swapcase(word)
  swap_case = word.chars.map do |char|
    if UPPERCASE.include?(char)
      char.downcase
    elsif LOWERCASE.include?(char)
      char.upcase
    else
      char
    end
  end
  swap_case.join
end
swapcase('CamelCase') == 'cAMELcASE'
swapcase('Tonight on XYZ-TV') == 'tONIGHT ON xyz-tv'

