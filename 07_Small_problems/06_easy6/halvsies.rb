def halvsies(array)
  counter = array.size
  array.partition do |num|
    counter -= 1
    counter >= array.size / 2
  end
end

halvsies([1, 2, 3, 4]) == [[1, 2], [3, 4]]
halvsies([1, 5, 2, 4, 3]) == [[1, 5, 2], [4, 3]]
halvsies([5]) == [[5], []]
halvsies([]) == [[], []]


